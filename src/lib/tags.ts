/**
 * Benchmark tagging — two competing schemes the user can compare side by side.
 *
 *  scheme "ability"  — the 10 operationalized cognitive abilities from
 *                      Benchmark Profiling (arXiv:2510.01232), plus domain
 *                      helper tags. Bottom-up: what skill does the task stress?
 *  scheme "survey"   — the benchmark taxonomy from Figure 2 of "A Survey on
 *                      LLM Benchmarks" (arXiv:2508.15361). Top-down: where does
 *                      the benchmark sit in the field's category tree?
 *
 * Each benchmark is tagged under BOTH schemes (data/benchmark-tags.json), so the
 * profiling page can toggle between them. Assignments are generated by
 * scripts/tag-benchmarks.ts.
 */

export interface TagDef {
  id: string;
  label: string;
  group: string;
  description: string;
}

export interface TagGroupDef {
  id: string;
  label: string;
  tags: TagDef[];
}

export interface TagScheme {
  id: "ability" | "survey";
  label: string;
  paper: string;
  url: string;
  blurb: string;
  groups: TagGroupDef[];
}

