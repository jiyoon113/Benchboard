// ── Tree helpers ─────────────────────────────────────────────────────────────
const SURVEY_NODE_BY_ID = new Map<string, TaxNode>();
const SURVEY_PARENT = new Map<string, string | null>();
(function index(nodes: TaxNode[], parent: string | null) {
  for (const n of nodes) {
    SURVEY_NODE_BY_ID.set(n.id, n);
    SURVEY_PARENT.set(n.id, parent);
    if (n.children) index(n.children, n.id);
  }
})(SURVEY_TREE, null);

export function isSurveyLeaf(id: string): boolean {
  const n = SURVEY_NODE_BY_ID.get(id);
  return !!n && !n.children;
}

/** All leaf ids beneath a node (or the node itself if it's a leaf). */
export function surveyDescendantLeaves(id: string): string[] {
  const n = SURVEY_NODE_BY_ID.get(id);
  if (!n) return [];
  if (!n.children) return [id];
  return n.children.flatMap((c) => surveyDescendantLeaves(c.id));
}

export const SURVEY_LEAF_IDS: string[] = SURVEY_TREE.flatMap((n) =>
  surveyDescendantLeaves(n.id),
);

/** Labels from root → leaf, e.g. ["Domain-Specific","Natural Sciences","Mathematics"]. */
export function surveyPath(leafId: string): string[] {
  const out: string[] = [];
  let cur: string | null = leafId;
  while (cur) {
    const n = SURVEY_NODE_BY_ID.get(cur);
    if (n) out.unshift(n.label);
    cur = SURVEY_PARENT.get(cur) ?? null;
  }
  return out;
}

export const SCHEMES = {
  ability: SCHEME_ABILITY,
  survey: SCHEME_SURVEY,
} as const;

export type SchemeId = "ability" | "survey";

const ALL_BY_ID = new Map<string, TagDef>();
for (const g of SCHEME_ABILITY.groups) for (const t of g.tags) ALL_BY_ID.set(t.id, t);

/** Resolve any tag/leaf id to a label (works for both schemes). */
export function tagById(id: string): TagDef | undefined {
  const a = ALL_BY_ID.get(id);
  if (a) return a;
  const n = SURVEY_NODE_BY_ID.get(id);
  if (n) return { id: n.id, label: n.label, group: "survey", description: n.description ?? "" };
  return undefined;
}

/** Valid tag ids of a scheme — used by the generator to validate output.
 *  For survey this is the leaf set. */
export function schemeTagIds(id: SchemeId): Set<string> {
  if (id === "ability") {
    return new Set(SCHEME_ABILITY.groups.flatMap((g) => g.tags.map((t) => t.id)));
  }
  return new Set(SURVEY_LEAF_IDS);
}
